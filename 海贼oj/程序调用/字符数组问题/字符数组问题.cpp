#include <stdio.h>
#include<stdlib.h>
#include<time.h>
#define MAX 20

int main()
{
    srand(time(0));
    char tryonce[MAX];
    int op = rand() % 10;
    for (int i = 0; i < op; i++)
    {
        printf_s("Please enter a string:\n");
        scanf_s("%s", tryonce, (unsigned)_countof(tryonce));
    }
    printf_s("good!\n");
    return 0;
}

